                    <p class="third-font-gray mt-4">La consulta ginecológica es esencial para la salud de la mujer, ya que permite detectar posibles enfermedades o trastornos en la zona genital y mamaria. Es importante acudir al ginecólogo al menos una vez al año para realizar un examen físico y una citología, lo que permite una detección temprana de cualquier patología que pudiera surgir.</p>
                    <p class="large third-font-gray mt-4 textoRosaOscuro"><b><strong>Mamografía 3D:</strong></b></p>
                    <p class="third-font-gray mt-4">La mamografía 3D es una técnica de diagnóstico por imagen que utiliza rayos X para obtener imágenes tridimensionales de la mama. Esta técnica es más precisa que la mamografía convencional y permite detectar pequeñas lesiones en las mamas que podrían ser indetectables en una mamografía convencional.</p>
                    <p class="large third-font-gray mt-4 textoRosaOscuro"><b><strong>Ecografía:</strong></b></p>
                    <p class="third-font-gray mt-4">La ecografía es una técnica de diagnóstico por imagen que utiliza ondas sonoras para obtener imágenes de la mama. Es una técnica no invasiva y sin radiación que permite detectar lesiones en la mama, especialmente en mujeres jóvenes o en mujeres con mamas densas.</p>
                    <p class="large third-font-gray mt-4 textoRosaOscuro"><b><strong>Resonancia magnética:</strong></b></p>
                    <p class="third-font-gray mt-4">La resonancia magnética es una técnica de diagnóstico por imagen que utiliza un imán y ondas de radio para obtener imágenes detalladas del interior del cuerpo. Es una técnica muy precisa que se utiliza principalmente para confirmar la presencia de un tumor en la mama y determinar su tamaño y ubicación.</p>
                    <p class="large third-font-gray mt-4 textoRosaOscuro"><b><strong>Cirugía:</strong></b></p>
                    <p class="third-font-gray mt-4">La cirugía es una de las opciones de tratamiento para el cáncer de mama. Existen diferentes tipos de cirugía, desde la extirpación del tumor hasta la mastectomía. La elección del tipo de cirugía dependerá del tamaño y la ubicación del tumor, así como de las características de la paciente. Es importante que la paciente consulte con su médico para determinar el mejor tratamiento para su caso en particular.</p>
